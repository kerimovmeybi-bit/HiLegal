document.addEventListener("DOMContentLoaded", () => {
  const params = new URLSearchParams(window.location.search);
  const id = params.get("id");

  const data = publications[id];

  if (!data) {
    console.warn("Publication not found");
    return;
  }

  // картинки
  document.querySelector("[data-img]").src = data.img;
  document.querySelector("[data-img2]").src = data.img2;

  // заголовки и дата
  document.querySelector("[data-title]").textContent = data.title;
  document.querySelector("[data-time]").textContent = data.time;
  document.querySelector("[data-title2]").textContent = data.title2;
  document.querySelector("[data-title-list]").textContent = data.titleList;
  document.querySelector("[data-title-list2]").textContent = data.titleList2;

  // текст
  document.querySelector("[data-text1]").textContent = data.text1;
  document.querySelector("[data-text2]").textContent = data.text2;
  document.querySelector("[data-text3]").textContent = data.text3;
  document.querySelector("[data-text4]").textContent = data.text4;
  document.querySelector("[data-text5]").textContent = data.text5;
  document.querySelector("[data-text6]").textContent = data.text6;
  document.querySelector("[data-text7]").textContent = data.text7;

  // список
  const list = document.querySelector("[data-list]");
  list.innerHTML = "";

  data.list.forEach(item => {
    const li = document.createElement("li");
    li.textContent = item;
    list.appendChild(li);
  });
});

